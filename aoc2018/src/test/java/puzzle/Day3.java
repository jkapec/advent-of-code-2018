    private boolean hasNotOverlap(Claim claim) {
        boolean overlaps = false;
        for (int x = 0; x < claim.width; x++) {
            for (int y = 0; y < claim.height; y++) {
                if (counts[claim.x + x][claim.y + y] > 1) {
                    overlaps = true;
                }
            }
        }

        return !overlaps;
    }

    private void addSquareInches(Claim claim) {
        for (int x = 0; x < claim.width; x++) {
            for (int y = 0; y < claim.height; y++) {
                counts[claim.x + x][claim.y + y]++;
            }
        }
    }

